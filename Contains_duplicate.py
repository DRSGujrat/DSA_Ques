#// Given an integer array nums, return true if any value appears more than once in the array, otherwise return false.

#// Example 1:

#// Input: nums = [1, 2, 3, 3]

#// Output: true

#// Example 2:

#// Input: nums = [1, 2, 3, 4]

#// Output: false

l1 = []

S1 = {i for i in l1}

if len(S1) == len(l1):
    print("False")
else:
    print("True")


